def reverse_list(input_list):
    reversed_list = input_list[::-1]
    return reversed_list

numbers = [1, 2, 3, 4, 5]
reversed_numbers = reverse_list(numbers)
print(reversed_numbers)  # Output: [5, 4, 3, 2, 1]

